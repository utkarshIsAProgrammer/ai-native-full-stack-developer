// String
let myString = 'Hello, world!';
console.log(myString, typeof myString);

// Number
let myNumber = 42;
let myFloat = 3.14;
console.log(myNumber, typeof myNumber);
console.log(myFloat, typeof myFloat);

// BigInt
let myBigInt = 9007199254740991n;
console.log(myBigInt, typeof myBigInt);

// Boolean
let myBoolean = true;
console.log(myBoolean, typeof myBoolean);

// undefined
let myUndefined;
console.log(myUndefined, typeof myUndefined);

// null
let myNull = null;
console.log(myNull, typeof myNull); // "object" (This is a known bug)
