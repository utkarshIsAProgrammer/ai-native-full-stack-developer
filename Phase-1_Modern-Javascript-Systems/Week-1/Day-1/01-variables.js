// Variables

// Making variables using var
var favLang = 'Javascript';
console.log(favLang);

// Re-declaring variables (only in var)
var favLang = 'Python';
console.log(favLang);

// Making variables using let
let role = 'Full Stack Developer';
console.log(role);

// Re-assigning value (in var and let)
role = 'ML Engineer';
console.log(role);

// Making variable using const
const EARTH_SHAPE = 'round';
console.log(EARTH_SHAPE);
